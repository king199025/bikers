<section class="about-knives">
  <div class="container">
    <h2 class="about-knives-title">О МОТОКЛУБЕ <span><a class="pokazat-2" href="">Посмотреть</a></span></h2>
		<div class="about-knives-hide">
		<div class="about-knives-content">
        <p class="about-knives-text">
          Мото клуб «Чёрные ножи» открылся 23 марта 2002 года и уже стал широко известен в Екатеринбурге, Уральском регионе и за его пределами, как демократичная организация любителей мотоциклетного спорта и туризма. Одна из задач мото клуба – поддержка российских патриотических традиций на основе преемственности поколений, любви к своей стране и городу. В связи с этим членами клуба было утверждено название «Чёрные ножи».
        </p>
          <p class="about-knives-text">
            Мото клуб назван в честь 10-го Гвардейского Уральского добровольческого танкового корпуса.
          </p>
          <p class="about-knives-text">
            24 февраля 1943 года по инициативе трудящихся Урала был сформирован 30-й Уральский добровольческий танковый корпус. Он был укомплектован добровольцами и оснащен оружием и военной техникой, изготовленной за счет добровольных взносов трудящихся Свердловской, Челябинской и Пермской областей. Рабочие прокатного цеха №3 Златоустовского машиностроительного завода изготовили и подарили бойцам и командирам танкового корпуса 3355 финских ножей черного цвета из специально закаленной стали. 12 марта 1943 года танковый корпус отправился на фронт с железнодорожного вокзала Свердловска. В последующем в честь гвардейского танкового корпуса был воздвигнут памятник на привокзальной площади, именуемый в народе «Варежкой».
          </p>
          <p class="about-knives-text">
            Первый бой 10-й Уральский добровольческий танковый корпус принял 27 июля 1943 года. Он участвовал в Орловской, Брянской, Черновицкой, Львовско-Сандомирской, Берлинской и Пражской наступательных операциях. В октябре 1943 года за проявленный личным составом в боях с немецко-фашистскими захватчиками героизм, отвагу, стойкость, дисциплинированность и высокое воинское мастерство преобразован в 10-й гвардейский Уральский добровольческий танковый корпус. Корпус удостоен почетного наименования «Львовский», награжден орденам Красного Знамени за умелые действия в овладении городами Леобштюц и Нейсе, орденом Суворова 2-й степени за образцовое выполнение заданий командования и орденом Кутузова 2-й степени за штурм Берлина. Свыше 42 тысяч воинов корпуса награждены орденами и медалями, 38 присвоено звание Героя Советского Союза.
          </p>
          <p class="about-knives-text">
            Бойцы танкового корпуса были настолько беспощадны к врагам и воевали так бесстрашно, что фашисты, которые боялись солдат, вооруженных ножами чёрного цвета, прозвали их «Чёрные ножи».

          </p>
          <p class="about-knives-text">
            За 22 месяца танковый корпус прошел с боями 3800 км и за это время в Москве в его честь прогремело 26 салютов, залпы первого из которых прозвучали уже 5 августа 1943 года.
          </p>

